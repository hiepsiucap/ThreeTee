export default function OrdersAdmin() {
  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(10);

  const handleChangePage = (event: unknown, newPage: number) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (
    event: React.ChangeEvent<HTMLInputElement>
  ) => {
    setRowsPerPage(+event.target.value);
    setPage(0);
  };

  return (
    <div className=" pl-72">
      <p className=" text-2xl py-6 ">Sản phẩm</p>
      <div className=" flex space-x-6 h-fit  ">
        <div className=" overflow-hidden h-fit rounded-2xl  shadow-lg w-2/3">
          <Paper sx={{ width: "100%", overflow: "hidden" }}>
            <TableContainer sx={{ maxHeight: 700 }}>
              <Table
                stickyHeader
                aria-label="sticky table"
              >
                <TableHead>
                  <TableRow>
                    {columns.map((column) => (
                      <TableCell
                        key={column.id}
                        align={column.align}
                        style={{ minWidth: column.minWidth }}
                      >
                        {column.label}
                      </TableCell>
                    ))}
                  </TableRow>
                </TableHead>
                <TableBody>
                  {rows
                    .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                    .map((row) => {
                      return (
                        <TableRow
                          hover
                          role="checkbox"
                          tabIndex={-1}
                          key={row.code}
                        >
                          {columns.map((column) => {
                            const value = row[column.id];
                            return (
                              <TableCell
                                key={column.id}
                                align={column.align}
                              >
                                {column.format && typeof value === "number"
                                  ? column.format(value)
                                  : value}
                              </TableCell>
                            );
                          })}
                        </TableRow>
                      );
                    })}
                </TableBody>
              </Table>
            </TableContainer>
            <TablePagination
              rowsPerPageOptions={[15]}
              component="div"
              count={rows.length}
              rowsPerPage={rowsPerPage}
              page={page}
              onPageChange={handleChangePage}
              onRowsPerPageChange={handleChangeRowsPerPage}
            />
          </Paper>
        </div>
        <div className=" flex flex-col space-y-6 w-1/3">
          <div className=" w-full bg-gray-50 p-6 rounded-3xl shadow-md">
            {" "}
            <BasicLine
              categories={categoriess}
              data={data}
              data1={data}
            />
          </div>
          <div className=" w-full bg-gray-50 p-6 rounded-3xl shadow-md">
            {" "}
            <BasicLine
              categories={categoriess}
              data={data}
              data1={data}
            />
          </div>
          <div className=" w-full bg-gray-50 p-6 rounded-3xl shadow-md">
            {" "}
            <BasicLine
              categories={categoriess}
              data={data}
              data1={data}
            />
          </div>
        </div>
      </div>
    </div>
  );
}
